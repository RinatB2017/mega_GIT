uint16_t CRC::java_crc16(uint8_t *pcBlock, uint8_t len)
{
    uint16_t crc = 0x0000;
    for(int n=0; n<len; n++)
    {
        crc = (crc >> 8) ^ Crc16Table[(crc ^ pcBlock[n]) & 0xff];
        //qDebug() << (QString("CRC: %1").arg(crc));
    }
#ifdef USE_HTONS
    return htons(crc);
#else
    return crc;
#endif
}
//--------------------------------------------------------------------------------
uint16_t CRC::crc16(uint8_t *pcBlock, uint8_t len)
{
    uint16_t a, crc = 0xFFFF;
    while(len--)
    {
        a = *pcBlock++ << 8;
        crc ^= a;
        for(int i=0; i<8; i++)
            crc = crc & 0x8000 ? (crc << 1) ^ 0x1021 : crc << 1;
    }
#ifdef USE_HTONS
    return htons(crc);
#else
    return crc;
#endif
}
//--------------------------------------------------------------------------------
uint16_t CRC::powersupply_crc16(uint8_t *pcBlock,
                                uint16_t len)
{
    uint16_t crc = 0xFFFF;

    for (int pos=0; pos<len; pos++)
    {
        crc ^= (uint16_t)pcBlock[pos];

        for (int i=8; i!=0; i--)
        {
            if ((crc & 0x0001) != 0)
            {
                crc >>= 1;
                crc ^= 0xA001;
            }
            else
                crc >>= 1;
        }
    }
#ifdef USE_HTONS
    return htons(crc);
#else
    return crc;
#endif
}
