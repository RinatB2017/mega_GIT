void MainBox::wait_msec(int timeout_msec)
{
    QElapsedTimer time;

#ifdef TEST
    timeout_msec = TEST_WAIT_MS;
#endif

    emit debug(QString("Пауза %1 ms").arg(timeout_msec));
    if(timeout_msec < 1)
    {
        emit debug("timeout_msec < 1");
        return;
    }
    time.start();
    while(time.elapsed() < timeout_msec)
    {
        QCoreApplication::processEvents();
    }
}
