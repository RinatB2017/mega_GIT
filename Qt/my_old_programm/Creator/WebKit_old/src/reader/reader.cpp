//--------------------------------------------------------------------------------
#include <QDebug>
//--------------------------------------------------------------------------------
#include "reader.hpp"
//--------------------------------------------------------------------------------
Reader::Reader(QWidget *parent) :
    QWebView(parent),
    is_loaded(false)
{

}
//--------------------------------------------------------------------------------
void Reader::set_main_url(const QUrl &url)
{
    emit debug(QString("main_url %1").arg(url.toString()));
    main_url = url;
}
//--------------------------------------------------------------------------------
void Reader::run(void)
{
    if(main_url.isEmpty())
    {
        qDebug() << "no url";
        return;
    }
    load(main_url);
    update();
}
