//--------------------------------------------------------------------------------
uint8_t CRC::crc8(uint8_t *pcBlock, uint8_t len)
{
    uint8_t crc = 0xFF;

    while (len--)
        crc = Crc8Table[crc ^ *pcBlock++];

    return crc;
}
//--------------------------------------------------------------------------------
uint8_t CRC::pelco_crc8(uint8_t *pcBlock, uint8_t len)
{
    uint8_t crc = 0;

    while (len--)
        crc += *pcBlock++;

    return crc;
}
