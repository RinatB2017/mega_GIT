#ifndef MAIN_HPP
#define MAIN_HPP

//---------------------------------------------------------------------------------------------------
class Main
{
public:

    Main()
    {
        x = 666;
    }

private:
    int x = 0;
};
//---------------------------------------------------------------------------------------------------
class A : virtual public Main
{
public:
    int get_x(void)
    {
        return x;
    }
private:
    int x = 1;
};
//---------------------------------------------------------------------------------------------------
class B : virtual public Main
{
public:
    int get_x(void)
    {
        return x;
    }
private:
    int x = 2;
};
//---------------------------------------------------------------------------------------------------
class C : public A, B
{
public:
    int get_x(void)
    {
        return B::get_x();
    }
};
//---------------------------------------------------------------------------------------------------
#endif // MAIN_HPP
